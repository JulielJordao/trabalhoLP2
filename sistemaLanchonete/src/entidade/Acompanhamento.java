package entidade;

public class Acompanhamento {
	private int cod_acomp;
	private int cod_promocao;
	private String nom_acomp;
	private double val_acomp;
	
	public int getCod_acomp() {
		return cod_acomp;
	}
	public void setCod_acomp(int cod_acomp) {
		this.cod_acomp = cod_acomp;
	}
	public int getCod_promocao() {
		return cod_promocao;
	}
	public void setCod_promocao(int cod_promocao) {
		this.cod_promocao = cod_promocao;
	}
	public String getNom_acomp() {
		return nom_acomp;
	}
	public void setNom_acomp(String nom_acomp) {
		this.nom_acomp = nom_acomp;
	}
	public double getVal_acomp() {
		return val_acomp;
	}
	public void setVal_acomp(double val_acomp) {
		this.val_acomp = val_acomp;
	}
	
}
